    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
        if(!(this.txtUsuario.equals("")) && !(this.txtContra.equals("")))
        {
            CtrlLogin objeLogin = new CtrlLogin();
            switch(objeLogin.verificarUsua(this.txtUsuario.getText(),this.txtContra.getText()))
            {
                case 0:
                    JOptionPane.showMessageDialog(this, "No existen sus datos en el registro");
                    break;
                case 3:
                    JOptionPane.showMessageDialog(this, "Bienvenido");
                    frmAdmin obje = new frmAdmin();
                    obje.setVisible(true);
                    this.dispose();
                        break;
                case 4:
                    JOptionPane.showMessageDialog(this, "Bienvenido");
                    frmEmple obje2 = new frmEmple();
                    obje2.setVisible(true);
                    this.dispose();
                    break;
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Debe ingresar los datos para verificar");
        }
        
        
        
    }//GEN-LAST:event_btnIngresarActionPerformed

